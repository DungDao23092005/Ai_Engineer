#Chuyển đổi kiểu dữ liệu (Type Casting) là quá trình chuyển đổi một giá trị từ kiểu dữ liệu này sang kiểu dữ liệu khác.
# Trong Python, chúng ta có thể sử dụng các hàm tích hợp để thực hiện việc này, chẳng hạn như int(), float(), str(), bool(). Dưới đây là một số ví dụ về cách sử dụng Type Casting trong Python:
# Chuyển đổi từ string sang int
age_str = "21"
age_int = int(age_str)
print(f"Tuoi cua toi la {age_int}.")

