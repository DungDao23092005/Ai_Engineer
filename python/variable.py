# Variable example chuỗi(string), số nguyên(integer), số thực(float), boolean
# Variable là một tên đại diện cho một giá trị nào đó. Chúng ta có thể gán giá trị cho biến và sử dụng nó trong chương trình.
# String là một chuỗi ký tự, được đặt trong dấu nháy đơn hoặc dấu nháy kép. Ví dụ: "Hello", 'World', "123", 'True'.
first_name = "Dung"
last_name = "Dao"
food = "Pizza"

print(first_name)
print(last_name)

print(f"Hello, {first_name} {last_name}!")
print(f"I like {food}!")
# Integer là một số nguyên, không có phần thập phân. Ví dụ: 1, 2, 3, -5, 0.
age=21
print(f"I am {age} years old.")

# Float là một số thực, có phần thập phân. Ví dụ: 3.14, -0.5, 0.0.
pi = 3.14
gpa = 3.75
print(f"so pi la {pi}")
print(f"gpa cua toi la {gpa}")

# Boolean là một giá trị logic, chỉ có hai giá trị: True (đúng) và False (sai). Ví dụ: True, False.
is_student = 1
is_employed = 0
print(f"Ban co phai la sinh vien? {is_student}")
print(f"Ban co phai la nhan vien? {is_employed}")